// switch case statements
// conditional statements
var age = 50;

switch (age) {
    case 20:
        console.log("You are young");
        break;
    case 30:
        console.log("You are still young");
        break;
    case 40:
        console.log("You rock");
        break;
    default:
        console.log("Invalid");
        break;
}